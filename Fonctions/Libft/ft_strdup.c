#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*str;

	str = malloc(ft_strlen((char *)s) + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
